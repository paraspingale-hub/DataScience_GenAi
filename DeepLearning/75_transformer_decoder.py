print(norm1)

# ------------------------------------------------------------
# Step 6: Encoder-Decoder Attention
# ------------------------------------------------------------

def encoder_decoder_attention(decoder_X, encoder_output):
    print("\nSTEP 6: Encoder-Decoder Attention")
    print("-" * 70)

    print("\nImportant Difference:")
    print("Q comes from decoder.")
    print("K and V come from encoder.")

    Wq = np.eye(4)
    Wk = np.eye(4)
    Wv = np.eye(4)

    Q = np.dot(decoder_X, Wq)
    K = np.dot(encoder_output, Wk)
    V = np.dot(encoder_output, Wv)

    print("\nQ Matrix from Decoder")
    print(Q)

    print("\nK Matrix from Encoder")
    print(K)

    print("\nV Matrix from Encoder")
    print(V)

    scores = np.dot(Q, K.T)

    print("\nScores = Qdecoder × Kencoder.T")
    print(scores)

    dk = K.shape[1]
    scaled_scores = scores / np.sqrt(dk)

    print("\nScaled Scores")
    print(scaled_scores)

    attention_weights = np.array([softmax(row) for row in scaled_scores])

    print("\nEncoder-Decoder Attention Weights")
    print(attention_weights)

    output = np.dot(attention_weights, V)

    print("\nEncoder-Decoder Attention Output")
    print(output)

    print("\nMeaning:")
    print("Decoder uses encoder output to understand which input words are important.")
    print("This connects target sentence generation with source sentence meaning.")

    return output

enc_dec_attention_output = encoder_decoder_attention(norm1, encoder_output)

# ------------------------------------------------------------
# Step 7: Add & Layer Normalization
# ------------------------------------------------------------

print("\nSTEP 7: Add & Layer Normalization after Encoder-Decoder Attention")
print("-" * 70)

add2 = norm1 + enc_dec_attention_output

print("\nAdd2 = Norm1 + Encoder-Decoder Attention Output")
print(add2)

norm2 = layer_norm(add2)

print("\nNorm2")
print(norm2)

