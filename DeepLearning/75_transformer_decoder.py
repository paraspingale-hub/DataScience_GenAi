# Step 4: Masked Self-Attention
# ------------------------------------------------------------

def masked_self_attention(X):
    print("\nSTEP 4: Masked Self-Attention")
    print("-" * 70)

    Wq = np.eye(4)
    Wk = np.eye(4)
    Wv = np.eye(4)

    Q = np.dot(X, Wq)
    K = np.dot(X, Wk)
    V = np.dot(X, Wv)

    print("\nQ Matrix")
    print(Q)

    print("\nK Matrix")
    print(K)

    print("\nV Matrix")
    print(V)

    scores = np.dot(Q, K.T)

    print("\nOriginal Scores = Q × K.T")
    print(scores)

    dk = K.shape[1]
    scaled_scores = scores / np.sqrt(dk)

    print("\nScaled Scores")
    print(scaled_scores)

    mask = np.triu(np.ones(scaled_scores.shape), k=1)

    print("\nMask Matrix")
    print(mask)

    masked_scores = scaled_scores.copy()
    masked_scores[mask == 1] = -1e9

    print("\nMasked Scores")
    print(masked_scores)

    attention_weights = np.array([softmax(row) for row in masked_scores])

    print("\nMasked Attention Weights")
    print(attention_weights)

    output = np.dot(attention_weights, V)

    print("\nMasked Self-Attention Output")
    print(output)

    print("\nMeaning:")
    print("Decoder cannot see future words.")
    print("For example, first word can attend only to itself.")

    return output

masked_attention_output = masked_self_attention(decoder_input_with_position)

