# Step 8: Feed Forward Network
# ------------------------------------------------------------

def feed_forward(X):
    print("\nSTEP 8: Feed Forward Network")
    print("-" * 70)

    W1 = np.array([
        [0.2, 0.4, 0.1, 0.3],
        [0.5, 0.1, 0.6, 0.2],
        [0.3, 0.7, 0.2, 0.5],
        [0.6, 0.2, 0.4, 0.1]
    ])

    W2 = np.array([
        [0.3, 0.2, 0.5, 0.1],
        [0.6, 0.4, 0.2, 0.3],
        [0.1, 0.7, 0.3, 0.5],
        [0.2, 0.1, 0.6, 0.4]
    ])

    hidden = np.dot(X, W1)

    print("\nDense Layer 1 Output")
    print(hidden)

    relu_output = np.maximum(0, hidden)

    print("\nAfter ReLU")
    print(relu_output)

    output = np.dot(relu_output, W2)

    print("\nDense Layer 2 Output")
    print(output)

    print("\nMeaning:")
    print("Feed Forward Network refines each output word representation.")

    return output

ff_output = feed_forward(norm2)

# ------------------------------------------------------------
# Step 9: Add & Layer Normalization Again
# ------------------------------------------------------------

print("\nSTEP 9: Add & Layer Normalization after Feed Forward")
print("-" * 70)

add3 = norm2 + ff_output

print("\nAdd3 = Norm2 + Feed Forward Output")
print(add3)

decoder_output = layer_norm(add3)

print("\nFinal Decoder Output")
print(decoder_output)

